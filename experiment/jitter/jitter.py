def time_to_delta_time(timelist):
  delta_time = []

  n = len(timelist)
  for i in range(n - 1):
    dt = timelist[i+1] - timelist[i]
    delta_time.append(dt)
  # print delta_time
  return delta_time

def delta_to_jitter(deltalist):
  jitter_list = []

  n = len(deltalist)
  for i in range(n - 1):
    jit = deltalist[i+1] - deltalist[i]
    jit = abs(jit)
    jitter_list.append(jit)
  # print jitter_list
  return jitter_list

